package CarPriceCalculator;

public class SalaryBean {
	public static double salary = 0;
	public static double yearlySalary = 0;

	public static double getSalary() {
		return salary;
	}

	public static void setSalary(double salary) {
		SalaryBean.salary = salary;
	}

	public static double getYearlySalary() {
		return yearlySalary;
	}

	public static void setYearlySalary(double yearlySalary) {
		SalaryBean.yearlySalary = yearlySalary;
	}
	
}
